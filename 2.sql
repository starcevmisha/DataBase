
select  Товары.КодТипа,  month([dbo].[Заказы].ДатаИсполнения) Месяц, Sum([dbo].[Заказано].Цена* Количество) ВсегоЗаказ
from dbo.[Заказы]

left join Заказано
on Заказано.КодЗаказа = [dbo].[Заказы].КодЗаказа

left join [dbo].[Товары]
on [dbo].[Товары].КодТовара = Заказано.КодТовара

where month(ДатаИсполнения) = 7 and year(ДатаИсполнения) = 1996

group by КодТипа, month(ДатаИсполнения)
